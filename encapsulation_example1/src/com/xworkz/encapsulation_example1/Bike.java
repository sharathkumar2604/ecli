package com.xworkz.encapsulation_example1;

public class Bike {

	
private String name;
private String color;
private int cost;
	

public Bike()
{
	
	
}


public String getName() {
	return name;
}


public void setName(String name) {
	this.name = name;
}


public String getColor() {
	return color;
}


public void setColor(String color) {
	this.color = color;
}


public int getCost() {
	return cost;
}


public void setCost(int cost) {
	this.cost = cost;
}

}
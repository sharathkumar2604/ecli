package com.xworkz.aptitude_Question;

public class SimpleIfElseExample {
public static void main(String[] args) {
	
	int a=10;
	int b=20;
	
	if(a<=b)
	{
		
		System.out.println(a+":is lesser than"+b);
		
		
	}
	else
	{
		System.out.println(a+":is greater than"+b);
		
	}
	
	
	if(true)
	{
		System.out.println("hi i'am true");
		
	}
	else
	{
		System.out.println("bye i'am not true");
	}
	
	if(false)
	{
		System.out.println("hi i'am true");
	}
	else
	{
		System.out.println("bye i'am false");
	}
}
}
